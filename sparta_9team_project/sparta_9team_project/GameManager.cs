                {
                    Console.SetCursorPosition(40, 23);
                    Console.WriteLine("잘못된 입력입니다.");
                    Console.SetCursorPosition(40, 24);
                    Console.WriteLine("다시 입력해주세요.");
                    Thread.Sleep(1000);
                }
            }
        }

