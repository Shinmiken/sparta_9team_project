                Console.WriteLine("========================================");
                Console.SetCursorPosition(40, 16);
                Console.ForegroundColor = ConsoleColor.Red;
                Console.WriteLine($"공격력 : {player.Atk}");
                Console.ForegroundColor = ConsoleColor.Blue;
                Console.SetCursorPosition(40, 17);
