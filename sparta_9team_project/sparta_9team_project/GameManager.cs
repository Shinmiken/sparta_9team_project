        //게임 메인화면
        public static void MainScreen()
        {
            Console.Clear();
            ConsoleManager.ColorPrintAsciiAt(ConsoleColor.White, Print.dogImage[0], 35, 3);
            ConsoleManager.PrintAnywhere("✦･ﾟ:* 1. 상태보기 *:･ﾟ✦", 50, 24);
